-- ---------- Dashboard / console views ----------

create or replace view dashboard_kpis as  SELECT count(*) AS total,
    count(*) FILTER (WHERE gate1_result = 'PASS'::text) AS gate1_pass,
    count(*) FILTER (WHERE gate1_result = 'VETOED'::text) AS gate1_vetoed,
    count(*) FILTER (WHERE gate2_result = 'PASS'::text) AS gate2_pass,
    count(*) FILTER (WHERE brief_generated) AS briefs_generated,
    count(*) FILTER (WHERE human_approved) AS approved,
    count(*) FILTER (WHERE gate1_result = 'FAIL'::text AND gate2_result = 'PASS'::text) AS mismatch
   FROM pipeline_runs;

create or replace view dashboard_coverage as  SELECT COALESCE(coverage_label, 'Unknown'::text) AS label,
    count(*) AS n FROM pipeline_runs GROUP BY (COALESCE(coverage_label, 'Unknown'::text));

create or replace view dashboard_trigger_type as  SELECT
        CASE WHEN trigger_type IS NULL OR (TRIM(BOTH FROM trigger_type) = ANY (ARRAY[''::text, ':'::text])) THEN 'Unknown'::text
            ELSE split_part(trigger_type, ':'::text, 1) END AS label,
    count(*) AS n FROM pipeline_runs GROUP BY 1;

create or replace view dashboard_trend as  SELECT date_trunc('day'::text, (run_at AT TIME ZONE 'Asia/Singapore'::text)) AS day,
    count(*) AS n FROM pipeline_runs GROUP BY 1 ORDER BY 1;

create or replace view dashboard_error_health as  SELECT phase, node_name,
    count(*) AS errors, count(*) FILTER (WHERE NOT resolved) AS unresolved
   FROM pipeline_errors GROUP BY phase, node_name;

create or replace view dashboard_action_funnel as  SELECT count(*) AS total,
    count(*) FILTER (WHERE status <> 'cancelled'::text) AS recommended,
    count(*) FILTER (WHERE status = ANY (ARRAY['executed'::text, 'measured'::text])) AS executed,
    count(*) FILTER (WHERE status = 'measured'::text) AS measured,
    count(*) FILTER (WHERE status = 'cancelled'::text) AS cancelled
   FROM actions;

create or replace view dashboard_hypothesis_winrate as  SELECT a.action_type,
    count(o.id) AS measured,
    count(o.id) FILTER (WHERE o.result = 'met'::text) AS met,
    round(100.0 * count(o.id) FILTER (WHERE o.result = 'met'::text)::numeric / NULLIF(count(o.id), 0)::numeric, 0) AS win_rate_pct
   FROM actions a LEFT JOIN outcomes o ON o.action_id = a.id GROUP BY a.action_type;

create or replace view console_pending_briefs as  SELECT id, company, brief_s3_url, when_urgency, when_stage,
    to_char((run_at AT TIME ZONE 'Asia/Singapore'::text), 'MM-DD HH24:MI'::text) AS run_sgt
   FROM pipeline_runs
  WHERE human_approved = false AND brief_generated = true AND COALESCE(notes, ''::text) !~~* '%reject%'::text
  ORDER BY run_at DESC LIMIT 50;

create or replace view console_pending_actions as  SELECT a.id, a.account, a.action_type, a.hypothesis, a.predicted_metric,
    a.tracked_url, a.run_id, p.brief_s3_url, p.when_urgency, p.when_stage, a.created_at
   FROM actions a LEFT JOIN pipeline_runs p ON p.id = a.run_id
  WHERE a.status = 'recommended'::text ORDER BY a.created_at DESC;

create or replace view dashboard_rejection_reasons as  SELECT COALESCE(rejection_reason, '(unspecified)'::text) AS label,
    COALESCE(rejection_routed_to, '-'::text) AS routed_to, count(*) AS n
   FROM actions WHERE status = 'cancelled'::text GROUP BY rejection_reason, rejection_routed_to ORDER BY count(*) DESC;

create or replace view dashboard_correction_rate as  SELECT (decided_at AT TIME ZONE 'Asia/Singapore'::text)::date AS cycle,
    count(*) AS decided, count(*) FILTER (WHERE status = 'cancelled'::text) AS corrected,
    round(100.0 * count(*) FILTER (WHERE status = 'cancelled'::text)::numeric / NULLIF(count(*), 0)::numeric, 0) AS correction_rate_pct
   FROM actions WHERE decided_at IS NOT NULL GROUP BY 1 ORDER BY 1;

create or replace view attribution_last_touch as
 WITH conversions AS (SELECT * FROM engagement_events WHERE event_type = ANY (ARRAY['meeting_booked'::text, 'stage_change'::text, 'form_fill'::text])),
      touches AS (SELECT * FROM engagement_events WHERE event_type = ANY (ARRAY['email_click'::text, 'ad_click'::text, 'page_visit'::text, 'reply'::text]))
 SELECT c.id AS conversion_id, c.account, c.event_type AS conversion, c.occurred_at AS converted_at,
    t.campaign AS credited_campaign, t.source AS credited_source, t.occurred_at AS touch_at
   FROM conversions c
     LEFT JOIN LATERAL (SELECT * FROM touches t_1 WHERE t_1.account = c.account AND t_1.occurred_at <= c.occurred_at
          ORDER BY t_1.occurred_at DESC LIMIT 1) t ON true;

create or replace view dashboard_attribution_summary as  SELECT COALESCE(credited_campaign, '(direct/none)'::text) AS campaign,
    COALESCE(credited_source, '(direct/none)'::text) AS source, count(*) AS conversions
   FROM attribution_last_touch GROUP BY 1, 2 ORDER BY count(*) DESC;

create or replace view dashboard_channel_performance as
 WITH touches AS (SELECT source AS channel, count(*) AS touches FROM engagement_events
          WHERE event_type = ANY (ARRAY['email_open'::text, 'email_click'::text, 'ad_click'::text, 'page_visit'::text, 'reply'::text]) GROUP BY source),
      conv AS (SELECT credited_source AS channel, count(*) AS conversions FROM attribution_last_touch
          WHERE credited_source IS NOT NULL GROUP BY credited_source)
 SELECT COALESCE(t.channel, c.channel) AS channel, COALESCE(t.touches, 0::bigint) AS touches,
    COALESCE(c.conversions, 0::bigint) AS conversions,
    round(100.0 * COALESCE(c.conversions, 0::bigint)::numeric / NULLIF(COALESCE(t.touches, 0::bigint), 0)::numeric, 0) AS conv_rate_pct
   FROM touches t FULL JOIN conv c ON t.channel = c.channel ORDER BY 3 DESC, 2 DESC;

-- Segmentation + enrichment
create or replace view dashboard_by_industry as  SELECT COALESCE(NULLIF(industry, ''::text), 'Unknown'::text) AS label,
    count(*) AS n FROM pipeline_runs GROUP BY 1 ORDER BY count(*) DESC;

create or replace view dashboard_by_theme as  SELECT COALESCE(NULLIF(theme, ''::text), 'Unknown'::text) AS label,
    count(*) AS n FROM pipeline_runs GROUP BY 1 ORDER BY count(*) DESC;

create or replace view dashboard_by_trigger as  SELECT COALESCE(NULLIF(trigger_notes, ''::text), 'Unknown'::text) AS label,
    count(*) AS n FROM pipeline_runs GROUP BY 1 ORDER BY count(*) DESC;

create or replace view dashboard_enrichment as  SELECT count(*) FILTER (WHERE enrichment_needed) AS needs_enrichment,
    count(*) FILTER (WHERE enrichment_needed = false) AS enriched, count(*) AS total FROM pipeline_runs;

-- Feedback routing views (rejections)
create or replace view feedback_for_tabita as  SELECT id, account, action_type, rejection_reason, decided_at
   FROM actions WHERE status = 'cancelled'::text AND rejection_routed_to = 'tabita'::text ORDER BY decided_at DESC;

create or replace view feedback_for_engine as  SELECT id, account, action_type, rejection_reason, decided_at
   FROM actions WHERE status = 'cancelled'::text AND rejection_routed_to = 'engine'::text ORDER BY decided_at DESC;

