-- ---------- Core tables ----------

create table if not exists pipeline_runs (
  id integer primary key generated by default as identity,
  run_at timestamptz default now(),
  company text,
  trigger_type text,
  is_actionable boolean,
  coverage_label text,
  engaged_count integer,
  total_count integer,
  gate1_score integer,
  gate1_threshold integer,
  gate1_confidence text,
  gate1_rationale text,
  gate1_vetoed boolean,
  gate1_veto_reason text,
  gate1_result text,
  gate2_result text,
  brief_generated boolean,
  brief_s3_url text,
  human_approved boolean,
  notes text,
  when_urgency text,
  when_stage text,
  cap_archetype text,
  cap_confidence text,
  source text,
  raw_payload text,          -- original WHEN-layer brief JSON (for rejection echo)
  industry text,             -- segmentation
  theme text,                -- segmentation
  trigger_notes text,        -- segmentation (WHEN change_triggers.notes)
  enrichment_needed boolean  -- buying group has unconfirmed contacts
);

create table if not exists actions (
  id bigint primary key generated by default as identity,
  run_id integer references pipeline_runs(id),
  account text,
  action_type text,
  hypothesis text,
  predicted_metric text,
  predicted_direction text,
  threshold numeric,
  window_days integer,
  utm_campaign text,
  status text,               -- recommended | executed | measured | cancelled
  created_at timestamptz default now(),
  executed_at timestamptz,
  tracked_url text,
  rejection_reason text,
  rejection_routed_to text,  -- tabita | engine
  decided_at timestamptz,
  outreach_draft text        -- WHEN engine's ready-made outreach copy
);

create table if not exists outcomes (
  id bigint primary key generated by default as identity,
  action_id bigint references actions(id),
  measured_metric text,
  measured_value numeric,
  baseline_value numeric,
  effect numeric,
  result text,               -- met | not_met
  source text,               -- ga4 | gmail
  window_start timestamptz,
  window_end timestamptz,
  measured_at timestamptz default now(),
  notes text
);

create table if not exists pipeline_errors (
  id integer primary key generated by default as identity,
  occurred_at timestamptz default now(),
  company text,
  phase text,
  node_name text,
  error_msg text,
  resolved boolean default false
);

-- Raw engagement events (feeds attribution + channel-performance views)
create table if not exists engagement_events (
  id bigint primary key generated by default as identity,
  account text,
  contact text,
  source text,
  event_type text,           -- email_open | email_click | ad_click | page_visit | reply | meeting_booked | stage_change | form_fill
  campaign text,
  action_id bigint,
  value numeric,
  occurred_at timestamptz,
  metadata jsonb,
  ingested_at timestamptz default now()
);

